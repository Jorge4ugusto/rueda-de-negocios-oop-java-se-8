    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        Rubro rubro = new Rubro();
        fila = jTable1.getSelectedRow();
         
         String columnaNombre = (String) jTable1.getValueAt(fila,1);
         String columnaDescripcion = (String) jTable1.getValueAt(fila,2);
        
         rubro.setNombre(columnaNombre);
         rubro.setDescripcion(columnaDescripcion);
         jTextField2.setText(columnaNombre);
         jTextArea1.setText(columnaDescripcion);
         jButton3.setEnabled(true);
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        Rubro rubro = new Rubro();
        fila = jTable1.getSelectedRow();
        int columna = (int) jTable1.getValueAt(fila,0);
        rubro.setId(columna);
        try {
            rubro.dardeBajaRubro();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

