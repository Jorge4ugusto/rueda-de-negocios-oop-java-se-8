    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
       
       Participante par = new Participante();
        par.setNombre(txtNombre.getText());
        par.setApellido(txtApellido.getText());
        par.setCargo(txtCargo.getText());
        int  seleccionado = cbDocumento.getSelectedIndex();
        par.setDocumento((String) cbDocumento.getItemAt(seleccionado));
        
        seleccionado = cbGenero.getSelectedIndex();
        par.setGenero((String) cbGenero.getItemAt(seleccionado));
       
        par.setNum_documento(txtNum_documento.getText());
        par.setTelefono(txtTelefono.getText());
        par.setCelular(txtCelular.getText());
        par.setEmail(txtEmail.getText());
        try {
            par.crearParticipante();
        txtNombre.setText("");
        txtApellido.setText("");
        txtCargo.setText("");
        txtNum_documento.setText("");
        txtTelefono.setText("");
        txtCelular.setText("");
        txtEmail.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Participante par = new Participante();
        int columnaId = (int) tablaListado.getValueAt(fila,0);
        par.setId(columnaId);
        
        par.setNombre(txtNombre.getText());
        par.setApellido(txtApellido.getText());
        par.setCargo(txtCargo.getText());
        par.setNum_documento(txtNum_documento.getText());
        
        int seleccionado = cbDocumento.getSelectedIndex();
        par.setDocumento((String) cbDocumento.getItemAt(seleccionado));

        seleccionado = cbGenero.getSelectedIndex();
        par.setGenero((String) cbGenero.getItemAt(seleccionado));

        par.setTelefono(txtTelefono.getText());
        par.setCelular(txtCelular.getText());
        par.setEmail(txtEmail.getText());
        try {
            par.actualizarParticipante();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

