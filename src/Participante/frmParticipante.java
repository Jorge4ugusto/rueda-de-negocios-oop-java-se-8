    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        int fila = tablaListado.getSelectedRow();
        if (fila > 0) {
            txtNombre.setText(tablaListado.getValueAt(fila, 1).toString());
            txtApellido.setText(tablaListado.getValueAt(fila, 2).toString());
            txtCargo.setText(tablaListado.getValueAt(fila, 3).toString());
            txtNum_documento.setText(tablaListado.getValueAt(fila, 4).toString());
            cbDocumento.setSelectedItem(tablaListado.getValueAt(fila, 5));
            cbGenero.setSelectedItem(tablaListado.getValueAt(fila, 6));
            txtTelefono.setText(tablaListado.getValueAt(fila, 7).toString());
            txtCelular.setText(tablaListado.getValueAt(fila, 8).toString());
            txtEmail.setText(tablaListado.getValueAt(fila, 9).toString());
            btnGuardar.setEnabled(true);
            btnRegistrar.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "Porfavor seleccione una fila.");
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

